import assert from "node:assert/strict";
import { createHash } from "node:crypto";
import { existsSync, readdirSync, readFileSync, statSync, writeFileSync } from "node:fs";
import { basename, join, relative } from "node:path";

const rootDir = process.cwd();
const hostedBase = "/EternalRicochet/";
const distDir = join(rootDir, "dist");
const assetsDir = join(distDir, "assets");
const iconsDir = join(distDir, "icons");
const indexPath = join(distDir, "index.html");
const manifestPath = join(distDir, "manifest.webmanifest");
const serviceWorkerPath = join(distDir, "service-worker.js");

assert.ok(existsSync(distDir), "dist must exist. Run vite build before generating the service worker.");
assert.ok(existsSync(indexPath), "dist/index.html must exist.");
assert.ok(existsSync(assetsDir), "dist/assets must exist.");
assert.ok(existsSync(manifestPath), "dist/manifest.webmanifest must exist.");
assert.ok(existsSync(join(iconsDir, "icon.svg")), "dist/icons/icon.svg must exist.");
assert.ok(existsSync(join(iconsDir, "maskable-icon.svg")), "dist/icons/maskable-icon.svg must exist.");

const jsAssets = readdirSync(assetsDir).filter((file) => file.endsWith(".js")).sort();
const cssAssets = readdirSync(assetsDir).filter((file) => file.endsWith(".css")).sort();

assert.ok(jsAssets.length >= 1, "dist/assets must include at least one JavaScript bundle.");
assert.ok(cssAssets.length >= 1, "dist/assets must include at least one CSS bundle.");
assert.ok(jsAssets.every(isHashedBundle), `JavaScript assets must be hashed bundles: ${jsAssets.join(", ")}`);
assert.ok(cssAssets.every(isHashedBundle), `CSS assets must be hashed bundles: ${cssAssets.join(", ")}`);

const cacheCandidates = [
  { url: hostedBase, path: indexPath },
  { url: `${hostedBase}index.html`, path: indexPath },
  ...jsAssets.map((file) => ({ url: `${hostedBase}assets/${file}`, path: join(assetsDir, file) })),
  ...cssAssets.map((file) => ({ url: `${hostedBase}assets/${file}`, path: join(assetsDir, file) })),
  { url: `${hostedBase}manifest.webmanifest`, path: manifestPath },
  { url: `${hostedBase}icons/icon.svg`, path: join(iconsDir, "icon.svg") },
  { url: `${hostedBase}icons/maskable-icon.svg`, path: join(iconsDir, "maskable-icon.svg") },
];

for (const candidate of cacheCandidates) {
  assert.ok(candidate.url.startsWith(hostedBase), `${candidate.url} must stay under ${hostedBase}`);
  assert.equal(candidate.url.startsWith("http://"), false, `${candidate.url} must not be absolute HTTP.`);
  assert.equal(candidate.url.startsWith("https://"), false, `${candidate.url} must not be absolute HTTPS.`);
  assert.ok(existsSync(candidate.path), `${formatPath(candidate.path)} must exist for ${candidate.url}`);
  assertNoForbiddenCachePath(candidate.url);
}

const versionToken = createVersionToken(cacheCandidates);
const cacheName = `eternal-ricochet-app-shell-v${versionToken}`;
const precacheUrls = [...new Set(cacheCandidates.map((candidate) => candidate.url))];
const serviceWorkerSource = renderServiceWorker({ cacheName, precacheUrls, hostedBase });

writeFileSync(serviceWorkerPath, serviceWorkerSource, "utf8");

console.log(
  `Generated dist/service-worker.js with cache ${cacheName} and ${precacheUrls.length} app-shell URLs.`,
);

function isHashedBundle(file) {
  return /^index-[A-Za-z0-9_-]{8,}\.(css|js)$/.test(file);
}

function createVersionToken(candidates) {
  const hash = createHash("sha256");
  for (const candidate of candidates) {
    const stats = statSync(candidate.path);
    hash.update(candidate.url);
    hash.update(String(stats.size));
    hash.update(readFileSync(candidate.path));
  }
  return hash.digest("hex").slice(0, 12);
}

function assertNoForbiddenCachePath(url) {
  const forbiddenPathPattern = /\/(?:api|auth|login|logout|leaderboard|provider|firebase|supabase|analytics|telemetry|credential|credentials|secret|admin|moderation)(?:\/|$)/i;
  assert.equal(
    forbiddenPathPattern.test(url),
    false,
    `Service-worker precache URL must not include forbidden path ${url}`,
  );
}

function renderServiceWorker({ cacheName, precacheUrls, hostedBase }) {
  return `/* Generated by scripts/generate-service-worker.mjs. Do not edit dist output by hand. */
const CACHE_NAME = ${JSON.stringify(cacheName)};
const APP_CACHE_PREFIX = "eternal-ricochet-";
const HOSTED_BASE = ${JSON.stringify(hostedBase)};
const PRECACHE_URLS = ${JSON.stringify(precacheUrls, null, 2)};
const PRECACHE_PATHS = new Set(PRECACHE_URLS.map((url) => new URL(url, self.location.origin).pathname));
const APP_SHELL_FALLBACK = ${JSON.stringify(`${hostedBase}index.html`)};

self.addEventListener("install", (event) => {
  event.waitUntil(
    caches.open(CACHE_NAME).then((cache) => cache.addAll(PRECACHE_URLS)),
  );
});

self.addEventListener("activate", (event) => {
  event.waitUntil(
    caches.keys()
      .then((cacheNames) => Promise.all(
        cacheNames
          .filter((name) => name.startsWith(APP_CACHE_PREFIX) && name !== CACHE_NAME)
          .map((name) => caches.delete(name)),
      ))
      .then(() => self.clients.claim()),
  );
});

self.addEventListener("message", (event) => {
  if (event.data?.type === "SKIP_WAITING") {
    self.skipWaiting();
  }
});

self.addEventListener("fetch", (event) => {
  const request = event.request;
  if (request.method !== "GET") return;

  const url = new URL(request.url);
  if (url.origin !== self.location.origin) return;
  if (!url.pathname.startsWith(HOSTED_BASE)) return;
  if (isForbiddenRuntimePath(url.pathname)) return;

  if (PRECACHE_PATHS.has(url.pathname)) {
    event.respondWith(cacheFirst(request));
    return;
  }

  if (request.mode === "navigate" || isAppShellNavigation(request, url)) {
    event.respondWith(networkFirstNavigation(request));
  }
});

async function cacheFirst(request) {
  const cache = await caches.open(CACHE_NAME);
  const cached = await cache.match(request, { ignoreSearch: true });
  if (cached) return cached;

  const response = await fetch(request);
  if (response.ok) {
    await cache.put(request, response.clone());
  }
  return response;
}

async function networkFirstNavigation(request) {
  try {
    const response = await fetch(request);
    if (response.ok) {
      const cache = await caches.open(CACHE_NAME);
      await cache.put(APP_SHELL_FALLBACK, response.clone());
    }
    return response;
  } catch (error) {
    const cached = await caches.match(APP_SHELL_FALLBACK);
    if (cached) return cached;
    throw error;
  }
}

function isAppShellNavigation(request, url) {
  const acceptsHtml = request.headers.get("accept")?.includes("text/html") ?? false;
  return acceptsHtml && url.pathname.startsWith(HOSTED_BASE);
}

function isForbiddenRuntimePath(pathname) {
  return /\\/(?:api|auth|login|logout|leaderboard|provider|firebase|supabase|analytics|telemetry|credential|credentials|secret|admin|moderation)(?:\\/|$)/i.test(pathname);
}
`;
}

function formatPath(path) {
  return relative(rootDir, path);
}
